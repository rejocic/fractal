#include "libft.h"

int		ft_word_count(char *s, char c)
{
	int		i;
	int		wc;

	i = 0;
	wc = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && s[i] != '\0')
			wc++;
		while (s[i] != c && s[i] != '\0')
			i++;
		while (s[i] == c && s[i] != '\0')
			i++;
	}
	return (wc);
}
